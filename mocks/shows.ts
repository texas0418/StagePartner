export function getShowById(id: string): Show | undefined {
  return SHOWS.find(show => show.id === id);
}

export function getSongById(songId: string): { song: import('@/types').Song; show: Show } | undefined {
  for (const show of SHOWS) {
    const song = show.songs.find(s => s.id === songId);
    if (song) return { song, show };
  }
  return undefined;
}
